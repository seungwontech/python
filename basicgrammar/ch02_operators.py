# 산술 연산자
# 기호: +
# 의미: 더하기
# 결과: 3
print(1 + 2)

# 기호: -
# 의미: 빼기
# 결과: 4
print(5 - 1)

# 기호: *
# 의미: 곱하기
# 결과: 108
print(54 * 2)

# 기호: /
# 의미: 나누기
# 결과: 27.0
print(54 / 2)

# 기호: //
# 의미: 몫
# 결과: 27
print(54 // 2)

# 기호: %
# 의미: 나머지
# 결과: 1
print(55 % 2)

# 기호: **
# 의미: 거듭 제곱
# 결과: 25
print(5 ** 2)

# ---------------------------------
# 비교 연산자
a_num = 5
b_num = 2
# 기호: >
# 의미: 크다
# 결과: True
print(a_num > b_num)

# 기호: >=
# 의미: 크거나 같다
# 결과: True
print(a_num >= b_num)

# 기호: <
# 의미: 작다
# 결과: False
print(a_num < b_num)

# 기호: <=
# 의미: 작거나 같다
# 결과: False
print(a_num <= b_num)

# 기호: ==
# 의미: 같다
# 결과: False
print(a_num == b_num)

# 기호: !=
# 의미: 같지 않다
# 결과: True
print(a_num != b_num)

# ---------------------------------
# 논리연산자
# 기호: and
# 의미: 둘다 참이면 True
# 결과: False
print(3 < 5 and 7 < 5)

# 기호: or
# 의미: 하나라도 참이면 True
# 결과: True
print(3 < 5 or 7 < 5)

# 기호: not
# 의미: 반전
# 결과: False
print(not 3 < 5)

# ---------------------------------
# 멤버 연산자

# 기호: in
# 의미: 포함
# 결과: True
print('c' in 'cat')
# 기호: not in
# 의미: 미포함
# 결과: False
print('c' not in 'cat')
