# 산술 연산자
# + 더하기 3
print(1 + 2)

# - 빼기 4
print(5 - 1)

# * 곱하기 108
print(54 * 2)

# / 나누기 27.0
print(54 / 2)

# // 몫 27
print(54 // 2)

# % 나머지 0 ,1
