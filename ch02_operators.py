# 비교 연산자
a_num = 5
b_num = 2
# 기호: >
# 의미: 크다
# 결과: True
print(a_num > b_num)

# 기호: >=
# 의미: 크거나 같다
# 결과: True
print(a_num >= b_num)

# 기호: <
# 의미: 작다
# 결과: False
print(a_num < b_num)

# 기호: <=
# 의미: 작거나 같다
# 결과: False
print(a_num <= b_num)

# 기호: ==
# 의미: 같다
# 결과: False
print(a_num == b_num)

# 기호: !=
# 의미: 같지 않다
# 결과: True
print(a_num != b_num)